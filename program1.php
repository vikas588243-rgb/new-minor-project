<?php
$radius = 7.0;
$circleArea = M_PI * $radius * $radius;
echo "the area of circle" . $circleArea . "<br>";

$length = 10.0;
$width = 5.0;
$rectangleArea = $length * $width;
echo "the area of rectangle" . $rectangleArea . "<br>";

$base = 8.0;
$height = 6.0;
$triangleArea = 0.5 * $base * $height;
echo "the area of triangle" . $triangleArea . "<br>";

$paraBase = 12.0;
$paraHeight = 7.0;
$parallelogramArea = $paraBase * $paraHeight;
echo "the area of parallelogram" . $parallelogramArea . "\n";

$side = 9.0;
$squareArea = $side * $side;
echo "the area of square" . $squareArea . "\n";

$sideA = 5.0;
$sideB = 11.0;
$trapHeight = 4.0;
$trapezoidArea = 0.5 * ($sideA + $sideB) * $trapHeight;
echo "the area of trapezoid" . $trapezoidArea . "\n";



?>
